package ex_10_For_Loop;

public class Lab_103 {
    public static void main(String[] args) {
        for(float f = 0.0F; (double)f < 10.67; ++f) {
            System.out.println("Hi, Float -> " + f);
        }

        for(long i = 1L; i < 10L; ++i) {
            System.out.println(i);
        }

    }
}
