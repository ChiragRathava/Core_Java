package ex_04_Operators;

public class Lab_48_OR_AND_GATE {
    public static void main(String[] args) {

        // || OR
        System.out.println(true || true);
        System.out.println(true || false);
        System.out.println(false || true);
        System.out.println(true || false);

        // && AND
        System.out.println(true && true);
        System.out.println(true && false);
        System.out.println(false && true);
        System.out.println(true && false);

    }
}
