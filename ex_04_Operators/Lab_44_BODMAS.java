package ex_04_Operators;

/*
* B : bracket
* O : order (powers, indices or roots)
* D : division
* M : multiplication
* A : addition
* S : subtraction
* */
public class Lab_44_BODMAS {
    public static void main(String[] args) {
        System.out.println((9 * 3 / 9 + 1) * 3);
        /*
         * 9 * 3 -> 27
         * 27 / 9 -> 3
         * 3 + 1 -> 4
         * 4 * 3 -> 12 */
    }
}
