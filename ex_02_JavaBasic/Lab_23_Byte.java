public class Lab_23_Byte {
    public static void main(String[] args) {
        byte c = 10;
        /*
        *when the statement byte c = 10 is executed in Java
        *the JVM creates a variable named c of type byte and assigns it the value 10.
        * the value 10 is represented in binary format as 00001010.
        * The JVM allocates a specific memory location for the variable c.  */
    }
}
