package ex_02_JavaBasic;

public class Lab_24_PrintF {
    public static void main(String[] args) {
        int a = 10;
        System.out.printf("Your Variable name is %d",a);
        // %d -> int, byte, long, short - data
        // %s -> string
        // %f -> float
        // %b -> Boolean

        byte b = 10;
        System.out.println();
        System.out.printf("%d",b);

    }
}
