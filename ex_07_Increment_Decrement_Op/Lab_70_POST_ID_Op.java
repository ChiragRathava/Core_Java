package ex_07_Increment_Decrement_Op;

public class Lab_70_POST_ID_Op {
    public static void main(String[] args) {
        int a  = 10;
        System.out.println(++a);
        System.out.println(a);

        // POST increment = Print first and the increase
        int i_post = 10;
        System.out.println(i_post++);
        System.out.println(i_post);
    }
}
